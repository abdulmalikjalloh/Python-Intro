# Arithmetic expression evaluates to a number
num1 = 10
num2 = 5
num3 = 11
num4 = 2

# + operator : plus
print(f"Addition: {num1} + {num2} = {num1 + num2}")  # Addition
print(f"Division:  {num3} / {num4} = {num3 / num4}")  # Division /
print(f"Floor Division:  {num3} // {num4} = {num3 // num4}")  # floor division //
print(f"Modulus/Mod:  {num3} Mod {num4} = {num3 % num4}")  # Modulus/Mod %
